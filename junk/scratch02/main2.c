#include <stdio.h>

void multi(int *p, int ex){
    for (int i = 1; i <= ex; i++){
        *p += ex;
    }
}

int main(void){
    int a = 0;
    multi(&a, 3);
    printf("%d\n", a);
    return 0;
}

